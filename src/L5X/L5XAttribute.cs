namespace L5Sharp.L5X
{
    /// <summary>
    /// An enumeration of L5X element attributes for ease of referencing these value without using magic strings.
    /// </summary>
    public enum L5XAttribute
    {
        /// <summary>
        /// Represents the <see cref="Name"/> L5X attribute name.
        /// </summary>
        Name,
        
        /// <summary>
        /// Represents the <see cref="Description"/> L5X attribute name.
        /// </summary>
        Description,

        /// <summary>
        /// Represents the <see cref="Value"/> L5X attribute name.
        /// </summary>
        Value,
        
        /// <summary>
        /// Represents the <see cref="DataType"/> L5X attribute name.
        /// </summary>
        DataType,

        /// <summary>
        /// Represents the <see cref="Index"/> L5X attribute name.
        /// </summary>
        Index,
        
        /// <summary>
        /// Represents the <see cref="Operand"/> L5X attribute name.
        /// </summary>
        Operand,
        
        /// <summary>
        /// Represents the <see cref="Format"/> L5X attribute name.
        /// </summary>
        Format,
        
        /// <summary>
        /// Represents the <see cref="Length"/> L5X attribute name.
        /// </summary>
        Length,
        
        /// <summary>
        /// Represents the <see cref="Dimension"/> L5X attribute name.
        /// </summary>
        Dimension,
        
        /// <summary>
        /// Represents the <see cref="Radix"/> L5X attribute name.
        /// </summary>
        Radix,
        
        /// <summary>
        /// Represents the <see cref="ExternalAccess"/> L5X attribute name.
        /// </summary>
        ExternalAccess
    }
}